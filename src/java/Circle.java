package src.java;

public class Circle {

    private int x;
    private int y;

    public Circle(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //get setter for x
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    //get setter for y
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }

}
